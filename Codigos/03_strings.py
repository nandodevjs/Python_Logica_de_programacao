# Strings são textos dentro de aspas simples ou duplas

print('Isso é uma string')
print("Isso também é um string")

# Para usar aspas dentro de ums string, utilize um caracter de escape como o \
print('String \"top\"')

# Ou é só utilizar aspas simples para definir a string e aspas duplas para colocar a palavra/frase dentro de aspas
print('"Texto"')

# Caso queira que o caracter de escape apareça, basta colocar o r na frente de tudo
print(r'String \"top\"')
